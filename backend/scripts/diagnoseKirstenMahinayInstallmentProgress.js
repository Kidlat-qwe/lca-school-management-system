section('E — Verdict');
const issues = [];

if (firstEnrolledPhase !== 2) {
  issues.push(`First active enrollment is class phase ${firstEnrolledPhase}, expected 2`);
}
for (const [slot, invId, tp] of [
  [2, 311, 2],
  [3, 571, 3],
  [4, 1012, 4],
]) {
  const chain = chainByLocalPhase.get(slot);
  const inv = chain?.representative?.invoice_id;
  const target = parseTargetPhase(chain?.representative?.remarks);
  if (Number(inv) !== invId) issues.push(`Plan slot ${slot}: expected INV-${invId}, got ${inv || 'empty'}`);
  if (target !== tp) issues.push(`Plan slot ${slot}: expected TARGET_PHASE:${tp}, got ${target ?? 'none'}`);
}
const slot1 = normalizedPhases.find((p) => p.phase_number === 1);
if (slot1?.billing_kind !== 'late_start_gap') {
  issues.push(`Plan slot 1 should be late_start_gap, got ${slot1?.billing_kind || 'none'}`);
}
if (profileGeneratedCount !== 3) {
  issues.push(`profile.generated_count is ${profileGeneratedCount}, expected 3`);
}
if (paidPhases !== 2) issues.push(`paid_phases SQL is ${paidPhases}, expected 2`);
if (generatedPhases !== 3) issues.push(`generated_phases SQL is ${generatedPhases}, expected 3`);
if (paidCount !== 2) issues.push(`Student History paid count is ${paidCount}, expected 2`);
if (generatedCount !== 3) issues.push(`Student History generated count is ${generatedCount}, expected 3`);

if (issues.length === 0) {
  console.log('OK — Progress metrics align with late-start enrollment on class phase 2.');
  console.log(
    `Installment Logs: ${listNumerator}/${listDenominator} | ` +
      `Student History: ${addressedCount}/${totalPhases} complete, ` +
      `${historyPaidDisplay}/${historyDenominator} paid, ` +
      `${historyGeneratedDisplay}/${historyDenominator} generated`
  );
} else {
  console.log('ISSUES FOUND:');
  issues.forEach((msg, i) => console.log(`  ${i + 1}. ${msg}`));
}

await pool.end();
process.exit(issues.length > 0 ? 1 : 0);
