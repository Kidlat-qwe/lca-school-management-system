section('B — Installment Invoice Logs progress (list SQL)');
const listProgressRes = await query(
  `SELECT
     (SELECT COUNT(DISTINCT COALESCE(i.invoice_chain_root_id, i.invoice_id))::integer
      FROM invoicestbl i
      WHERE i.installmentinvoiceprofiles_id = $1
        AND i.status = 'Paid'
        AND (
          $2::int IS NULL OR
          COALESCE(i.invoice_chain_root_id, i.invoice_id) != $2::int
        )
     ) AS paid_phases,
     (SELECT COUNT(DISTINCT COALESCE(i.invoice_chain_root_id, i.invoice_id))::integer
      FROM invoicestbl i
      WHERE i.installmentinvoiceprofiles_id = $1
        AND (
          $2::int IS NULL OR
          COALESCE(i.invoice_chain_root_id, i.invoice_id) != $2::int
        )
     ) AS generated_phases,
     COALESCE(ip.generated_count, 0)::integer AS profile_generated_count,
     (SELECT MAX(cs.phase_number)::integer
      FROM classstudentstbl cs
      WHERE cs.student_id = ip.student_id
        AND cs.class_id = ip.class_id
        AND cs.removed_at IS NULL
        AND cs.phase_number IS NOT NULL
     ) AS last_enrolled_phase_number
   FROM installmentinvoiceprofilestbl ip
   WHERE ip.installmentinvoiceprofiles_id = $1`,
  [PROFILE_ID, profile.downpayment_invoice_id]
);
