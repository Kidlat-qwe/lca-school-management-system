section('C — Student History phases API (simulated)');
const downpaymentInvoiceId =
  profile.downpayment_invoice_id != null ? Number(profile.downpayment_invoice_id) : null;

const profileInvoices = invoices.filter((i) => Number(i.installmentinvoiceprofiles_id) === PROFILE_ID);
const chainMap = new Map();
for (const inv of profileInvoices) {
  const chainRoot = Number(inv.chain_root_id);
  if (!chainMap.has(chainRoot)) {
    chainMap.set(chainRoot, {
      chain_root_id: chainRoot,
      representative: inv,
      paid_amount: 0,
      latest_payment_date: null,
      invoices: [],
    });
  }
  const chain = chainMap.get(chainRoot);
  chain.invoices.push(inv);
  chain.paid_amount += Number(inv.paid_total || 0);
}

let downpaymentChain = null;
const phaseChains = [];
for (const chain of chainMap.values()) {
  if (downpaymentInvoiceId && chain.chain_root_id === downpaymentInvoiceId) {
    downpaymentChain = chain;
  } else if (!isCancelledInvoiceChain(chain)) {
    phaseChains.push(chain);
  }
}

const droppedAbsolutePhases = await loadDroppedAbsolutePhasesForProfile(pool, STUDENT_ID, CLASS_ID);
const chainByLocalPhase = mapPhaseChainsToLocalSlots(phaseChains, profile);

const hasLaterAbsoluteBilling = (absolutePhase) => {
  for (const local of chainByLocalPhase.keys()) {
    if (phaseStart + Number(local) - 1 > absolutePhase) return true;
  }
  return false;
};
const hasLaterAbsoluteEnrollment = (absolutePhase) => {
  for (const abs of activeEnrollmentPhases) {
    if (abs > absolutePhase) return true;
  }
  return false;
};

const graceDays = await resolveInstallmentGraceDays(pool, profile.branch_id);
const todayYmd = todayYmdManila();
const computeStatus = (invoiceStatus, dueDate, issueDate) =>
  computeInstallmentPhaseDisplayStatus({
    invoiceStatus,
    dueDateYmd: dueDate,
    issueDateYmd: issueDate,
    graceDays,
    todayYmd,
  });

const totalPhases = Number(profile.total_phases || 0);
const phases = [];
for (let localPhase = 1; localPhase <= totalPhases; localPhase += 1) {
  const chain = chainByLocalPhase.get(localPhase);
  if (!chain) {
    const absoluteGap = phaseStart + localPhase - 1;
    const isSkippedGap = droppedAbsolutePhases.has(absoluteGap);
    const isLateStartGap =
      !isSkippedGap &&
      !activeEnrollmentPhases.has(absoluteGap) &&
      (hasLaterAbsoluteBilling(absoluteGap) || hasLaterAbsoluteEnrollment(absoluteGap));
    phases.push({
      phase_number: localPhase,
      invoice_id: null,
      status: isLateStartGap ? null : 'Not Generated',
      is_generated: false,
      amount: isLateStartGap ? null : Number(profile.amount),
      paid_amount: isLateStartGap ? null : 0,
      billing_kind: isSkippedGap ? 'skipped_gap' : isLateStartGap ? 'late_start_gap' : null,
    });
    continue;
  }
  const rep = chain.representative;
  const amount = rep.amount != null ? Number(rep.amount) : null;
  const paidAmount = Number(chain.paid_amount || 0);
  const profilePhaseAmount = Number(profile.amount || 0);
  const expectedAmount = amount != null ? amount : profilePhaseAmount;
  let displayStatus = computeStatus(rep.status, rep.due_date, rep.issue_date);
  if (Math.max(0, expectedAmount - paidAmount) <= 0.009) displayStatus = 'Paid';
  else if (paidAmount > 0.009 && expectedAmount <= 0.009) displayStatus = 'Paid';
  phases.push({
    phase_number: localPhase,
    invoice_id: Number(rep.invoice_id),
    status: displayStatus,
    is_generated: true,
    is_auto_generated: isAutoGeneratedInstallmentInvoice(rep),
    amount,
    paid_amount: paidAmount,
    billing_kind: isAdvancePaymentInvoice(rep) ? 'advance' : null,
  });
}

let normalizedPhases = annotateInstallmentPhasePlanSlots(
  normalizeAdjacentPhaseDisplayDates(phases, computeStatus)
);
const enrollmentByPhase = await loadEnrollmentStatusByAbsolutePhase(query, STUDENT_ID, CLASS_ID);
normalizedPhases = attachEnrollmentToInstallmentPhaseRows(normalizedPhases, {
  phaseStart,
  enrollmentByAbsolutePhase: enrollmentByPhase,
});

console.log('\nPhase rows (plan slot → invoice):');
for (const p of normalizedPhases.slice(0, 6)) {
  console.log(
    `  Slot ${p.phase_number} (class ph. ${p.absolute_phase_number}): ` +
      `billing=${p.billing_kind || '—'} gen=${p.is_generated} status=${p.status || '—'} ` +
      `inv=${p.invoice_id || '—'} enrollment=${p.program_enrollment_status || '—'} ` +
      `addressed=${p.plan_slot_addressed}`
  );
}

const generatedCount = normalizedPhases.filter((p) => p.is_generated).length;
const paidCount = normalizedPhases.filter((p) => {
  const st = String(p.status || '').toLowerCase();
  return st === 'paid' || st === 'paid all';
}).length;
const addressedCount = normalizedPhases.filter((p) => p.plan_slot_addressed).length;
const lateStartGaps = normalizedPhases.filter((p) => p.billing_kind === 'late_start_gap').length;

const historyPaidDisplay = paidCount + phaseStartOffset;
const historyGeneratedDisplay = generatedCount + phaseStartOffset;
const historyDenominator = totalPhases + phaseStartOffset;

console.log('\nStudent History UI (InstallmentPlanDetails):');
console.log({
  phases_complete: `${addressedCount} / ${totalPhases}`,
  paid: `${historyPaidDisplay} / ${historyDenominator}`,
  generated: `${historyGeneratedDisplay} / ${historyDenominator}`,
  late_start_gap_slots: lateStartGaps,
});

