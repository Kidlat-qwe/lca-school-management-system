const lastEnrolledRelative =
  lastEnrolledPhase != null ? Math.max(0, lastEnrolledPhase - phaseStartOffset) : 0;
const displayPhaseProgress = Math.min(
  billedPhaseProgress,
  Number(profile.total_phases || 0) || billedPhaseProgress
);
